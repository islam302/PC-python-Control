import os
from dotenv import load_dotenv
import openai

load_dotenv()

class Config:
    """
    ????? ???????? ?????? ?? OpenAI ?????? ??????? ???????.
    """
    def __init__(self):
        self.verbose = True
        self.openai_api_key = os.getenv("OPENAI_API_KEY")

    def initialize_openai(self):
        if not self.openai_api_key:
            raise ValueError("???? ????? ????? OpenAI API.")
        openai.api_key = self.openai_api_key
        return openai

config = Config()
